int main(void)
{
  /* USER CODE BEGIN 1 */
	// The statring point of the controller is CFG_init() function in config.c file
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI2_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_4);
  RENC_createButton(&renc, encoder_B_GPIO_Port, encoder_B_Pin);
  RENC_createEncoder(&renc, encoder_R_GPIO_Port, encoder_R_Pin, encoder_L_GPIO_Port, encoder_L_Pin);
  u8g_InitComFn(&u8g, &u8g_dev_sh1106_128x64_hw_spi, u8g_com_hw_spi_fn);
  HAL_Delay(100);
  u8g_SetRot180(&u8g);

  IRON_init(&hadc1, &htim2, &htim3);

  MODE *pCurrentMode = MODE_init(&u8g, &renc, &hi2c1);
  pCurrentMode->init();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
    while (1) {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
    	MODE *next = pCurrentMode->loop();
    	if (next != pCurrentMode) {
    		IRON_switchPower(false);					// Prevent overheating, switch off the IRON when change the mode
    		RENC_intButtonStatus(&renc);				// Ignore button status
    		next->init();
    		pCurrentMode = next;
    	}
    }
  /* USER CODE END 3 */

}

